# pyton 中，定义变量时不需要指定变量的类型
# 在运行时，python解释器，会根据赋值语句等号右边的数据自动推导出变量中保存数据的准确类型

# str表示时一个字符串类型
name = "小明"
# int 表示一个整数类型
age = 18
# bool 表示一个布尔类型，真 True 或者 假 False
gender = True  # 是男生
# float 表示是一个小数类型，浮点数
height = 170
weight = 70
print(name, age, gender, height, weight)
